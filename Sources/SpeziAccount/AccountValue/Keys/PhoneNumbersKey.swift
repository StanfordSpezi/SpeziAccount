import Foundation
import SpeziFoundation
import SpeziViews
import SwiftUI


private struct DisplayView: DataDisplayView {
    private var phoneNumbers: [String]
    
    var body: some View {
        ForEach(phoneNumbers, id: \.self) { number in
            ListRow("Phone") {
                Text(number)
            }
        }
    }
    
    init(_ value: [String]) {
        self.phoneNumbers = value
    }
}

extension AccountDetails {
#if compiler(<6)
    // In older compilers, we get a circular reference error when trying to add an extension (see end of file) to a type that is generated by a macro
    // Therefore, we manually expand the macro for older versions of the compiler.

    public struct __Key_phoneNumbers: AccountKey { // swiftlint:disable:this type_name
        public typealias Value = [String]

        public static let name = LocalizedStringResource("PHONE_NUMBERS", bundle: .atURL(from: .module))
        public static let identifier = "phoneNumbers"
        public static let category: AccountKeyCategory = .contactDetails
        public struct DataEntry: DataEntryView {
            @Binding private var value: Value

            public var body: some View {
                PhoneNumberEntryView($value)
            }

            public init(_ value: Binding<Value>) {
                self._value = value
            }
        }
    }
#endif

#if compiler(>=6)
    public typealias PhoneNumbersArray = [String]

    /// The phone numbers of a user.
    @AccountKey(
        name: LocalizedStringResource("PHONE_NUMBERS", bundle: .atURL(from: .module)),
        category: .contactDetails,
        as: PhoneNumbersArray.self,
        displayView: DisplayView.self,
        entryView: PhoneNumbersEntryView.self
    )
    public var phoneNumbers: PhoneNumbersArray?
#else
    /// The phone number of a user.
    public var phoneNumbers: PhoneNumbersArray? {
        get {
            self [__Key_phoneNumbers.self]
        }
        set {
            self [__Key_phoneNumbers.self] = newValue
        }
    }
#endif
}


@KeyEntry(\.phoneNumbers)
public extension AccountKeys {} // swiftlint:disable:this no_extension_access_modifier
